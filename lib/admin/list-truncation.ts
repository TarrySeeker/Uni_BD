/**
 * Чистый помощник для плашки усечения списка (находка #9).
 *
 * Списки админки читаются с жёстким LIMIT (например, подписчики — 500) без
 * пагинации. Если записей больше лимита, владелец видел только последние N и
 * НИКАК об этом не узнавал. Эта функция формирует понятную плашку «Показаны
 * последние N из M» — но только когда список реально усечён.
 *
 * Усечение: показано ровно `limit` строк, а всего записей больше `limit`.
 * Возвращает текст плашки или null (плашку не показывать). Чистая, без БД/React.
 */
export function listTruncationNotice(
  shown: number,
  total: number,
  limit: number,
): string | null {
  if (shown >= limit && total > limit) {
    return `Показаны последние ${limit} из ${total}`;
  }
  return null;
}
