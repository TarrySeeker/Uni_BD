/**
 * Экранирование метасимволов LIKE/ILIKE для БУКВАЛЬНОГО поиска подстроки.
 *
 * В Postgres `%` (любая последовательность) и `_` (любой символ) — wildcards, а `\`
 * — escape по умолчанию. Без экранирования пользовательский ввод вроде «50%» или
 * «a_b» трактуется как шаблон → over-broad/неверные результаты. Оборачивать
 * результат в `%...%` нужно ПОСЛЕ экранирования: `%${escapeLike(q)}%`.
 */
export function escapeLike(input: string): string {
  return input.replace(/[\\%_]/g, (c) => '\\' + c);
}
