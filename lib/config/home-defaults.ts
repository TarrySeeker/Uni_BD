/**
 * Дефолты контента главной страницы витрины (ADR-018).
 *
 * Источник правды для ФОЛБЭКА: пока владелец не отредактировал блок главной
 * через админку, витрина показывает этот нейтральный «коробочный» контент.
 * Литералы намеренно обобщённые (без привязки к нише/бренду магазина) — это
 * заглушка-плейсхолдер, которую любой магазин переопределяет под себя.
 *
 * ВАЖНО (мультитенантность): эти значения — нейтральный дефолт платформы, а НЕ
 * хардкод конкретного магазина. Любой магазин переопределяет блоки через ключ
 * настроек `home` (shop_settings) без правки кода. Изображения здесь не заданы
 * (imageKey пуст) — витрина использует собственные ассеты, пока ключ не загружен.
 *
 * Семантика merge: строка БД хранит оверрайд блока ЦЕЛИКОМ; отсутствие блока в
 * БД → берётся соответствующий блок отсюда (см. mergeSettings в config/settings).
 */

/** Полностью разрешённый контент главной (все блоки заполнены, без undefined). */
export interface HomeContent {
  hero: {
    title: string | null;
    subtitle: string | null;
    /** Ключ S3 фонового изображения (не URL); пусто → ассет витрины. */
    imageKey: string | null;
    ctaLabel: string | null;
    ctaHref: string | null;
  };
  about: {
    title: string;
    paragraphs: string[];
    /** Ключи S3 изображений галереи «О бренде» (не URL). */
    imageKeys: string[];
    /** Короткие «ценности»-теги (ValuesStrip / список под галереей). */
    values: string[];
  };
  quality: {
    title: string;
    items: string[];
  };
  delivery: {
    items: { title: string; text: string }[];
  };
}

