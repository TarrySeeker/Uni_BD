/**
 * ЕДИНЫЙ предикат «товар виден на витрине» (общий корень разбора дефектов).
 *
 * Источник истины: Storefront API отдаёт товар по ОДНОМУ условию — `status =
 * 'active'` (см. lib/storefront/queries.ts и products/[slug]/route.ts), БЕЗ
 * требований к цене или остатку. Активный товар с остатком 0 витрина показывает
 * (с «Нет в наличии»), а не скрывает.
 *
 * Раньше админ-индикатор в ProductForm считал товар «скрыт с сайта» при остатке 0
 * (и без цены) — строже реального фильтра витрины → ложное «нет на витрине», хотя
 * товар на витрине есть. Чтобы индикатор и реальная выдача не расходились, обе
 * стороны опираются на этот предикат.
 *
 * Чистая функция, тестируема без БД/Next; переиспользуема для всех арендаторов.
 */

import type { ProductStatus } from './types';

/** Товар виден покупателям на витрине ⇔ статус «active». */
export function isPubliclyVisible(status: ProductStatus): boolean {
  return status === 'active';
}
